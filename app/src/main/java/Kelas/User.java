public class User {

    private String nama;
    private int coin;
    private int exp;
    private int rank;
    private int free_coin;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getCoin() {
        return coin;
    }

    public void setCoin(int coin) {
        this.coin = coin;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public int getFree_coin() {
        return free_coin;
    }

    public void setFree_coin(int free_coin) {
        this.free_coin = free_coin;
    }
}
